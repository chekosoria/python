"""Operaciones"""

# Suma

print(1 + 3)

# Resta

print(1 - 3)

# Multiplicación

print(1 * 3)

# División un / muestra decimales // muestra solo la parte entera

print(15 / 6)
print(15 // 6)

# Módulo - regresa el residuo de la división

print(15 % 6)

# Potencia

print(2 ** 3)

# También es posible reemplazar el valor de una variable tras aplicar
# una operación VARIABLE = VARIABLE + n o de una forma abreviada
# VARIABLE += n
# VARIABLE -= n
# VARIABLE *= n

NUMERO += 2

print(NUMERO)

NUMERO -= 2

print(NUMERO)

NUMERO *= 2

print(NUMERO)

NUMERO /= 2

print(NUMERO)

NUMERO //= 2

print(NUMERO)

