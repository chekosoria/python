"""Funciones para números"""
import math

# Para redondear

print(round(3.14))

# Para obtener el valor absoluto

print(abs(-33))

# Toma el número y lo lleva al número superior entero
# cercano

print(math.ceil(1.1))

# Toma el número y lo lleva al número inferior entero
# cercano

print(math.floor(1.999))

# Para verificar que el argumento siempre sea númerico, devuelve
# un False cuando SI es númerido y un error si es cadena

print(math.isnan(23))

# Potencia

print(math.pow(3, 3))

# Para obtener la raíz de un número

print(math.sqrt(9))
