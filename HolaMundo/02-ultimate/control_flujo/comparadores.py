"""Comparación de valores"""

# Definición de variables tipo entero
N1 = 10
N2 = 15

# Evalua si la variable 1 es mayor que la variable 2

print(N1 > N2)

# Evalua si la variable 1 es menor que la variable 2

print(N1 < N2)

# Evalua si la variable 1 es meno o igual a 10

print(N1 <= 10)

# Evalua si la variable 1 es mayor o igual a 10

print(N1 >= 10)

# Evalua si la variable 2 es igual a 20

print(N2 == 20)

# Evalua si la variable 2 es igual a 15

print(N2 == 15)
