"""Return en Python"""


def suma(a, b):
    """Función para retornar la suma de dos números"""
    return a + b


def multipicar(a, b):
    """Función para retornar la suma de dos números"""
    c = a * b
    return c


RESULTADO = suma(15, 5)
R2 = suma(RESULTADO, 40)

print(RESULTADO)
print(R2)

RESULTADO = multipicar(15, 5)
R2 = multipicar(RESULTADO, 40)

print(RESULTADO)
print(R2)
