"""Ciclo While"""

# Se agrega una lista como variable

lenguajes = ["Python", "Ruby", "PHP", "Javascript", "Java"]

# Definir contador

i = 1

# Comenzar con el ciclo while

while i <= 5:
    print(i)
    i += 1

# Definir contador

i = 1

# Comenzar con el ciclo while

while i <= 5:
    print(i * "*")
    i += 1

